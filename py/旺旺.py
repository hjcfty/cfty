    def extract_real_video_url(self, html_content):
        """
        从播放器页面提取真实的视频地址
        """
        try:
            # 方法1: 从JavaScript配置中提取（最常见）
            pattern1 = r'"url"\s*:\s*"([^"]+)"'
            pattern2 = r'url\s*:\s*["\']([^"\']+)["\']'
            pattern3 = r'src\s*:\s*["\']([^"\']+)["\']'
            
            patterns = [pattern1, pattern2, pattern3]
            
            for pattern in patterns:
                match = re.search(pattern, html_content)
                if match:
                    url = match.group(1)
                    if url and ('m3u8' in url.lower() or 'mp4' in url.lower() or 'm3u' in url.lower()):
                        # 清理URL
                        url = url.replace('\\/', '/')
                        return url
            
            # 方法2: 查找所有可能的视频链接
            video_patterns = [
                r'https?://[^"\'\s]+\.m3u8[^"\'\s]*',
                r'https?://[^"\'\s]+\.mp4[^"\'\s]*',
                r'https?://[^"\'\s]+\.m3u[^"\'\s]*',
                r'https?://[^"\'\s]*\.(?:m3u8|mp4|m3u)[^"\'\s]*'
            ]
            
            for pattern in video_patterns:
                matches = re.findall(pattern, html_content, re.IGNORECASE)
                for match in matches:
                    if match and ('m3u8' in match.lower() or 'mp4' in match.lower() or 'm3u' in match.lower()):
                        # 清理URL
                        match = match.replace('\\/', '/')
                        return match
            
            # 方法3: 从var config中提取
            config_pattern = r'var\s+config\s*=\s*\{[^}]+"url"\s*:\s*"([^"]+)"'
            config_match = re.search(config_pattern, html_content, re.DOTALL)
            if config_match:
                url = config_match.group(1)
                if url and ('m3u8' in url.lower() or 'mp4' in url.lower()):
                    url = url.replace('\\/', '/')
                    return url
            
            # 方法4: 从所有script标签中提取
            script_pattern = r'<script[^>]*>(.*?)</script>'
            scripts = re.findall(script_pattern, html_content, re.DOTALL | re.IGNORECASE)
            
            for script in scripts:
                # 在script中查找config对象
                config_patterns = [
                    r'config\s*=\s*\{[^}]+url\s*:\s*["\']([^"\']+)["\']',
                    r'var\s+config\s*=\s*\{[^}]+url\s*:\s*["\']([^"\']+)["\']'
                ]
                
                for config_pat in config_patterns:
                    match = re.search(config_pat, script, re.DOTALL)
                    if match:
                        url = match.group(1)
                        if url and ('m3u8' in url.lower() or 'mp4' in url.lower()):
                            url = url.replace('\\/', '/')
                            return url
                
                # 直接在script中查找视频链接
                for video_pat in video_patterns:
                    matches = re.findall(video_pat, script, re.IGNORECASE)
                    for match in matches:
                        if match and ('m3u8' in match.lower() or 'mp4' in match.lower()):
                            match = match.replace('\\/', '/')
                            return match
            
            return None
            
        except Exception as e:
            print(f"提取真实视频地址失败: {str(e)}")
            return None

